func Encrypt(content, key string) string {
	if str.IsBlank(content) {
		return content
	}
	origData := []byte(content) // 待加密的数据
	keyBytes := []byte(key)     // 加密的密钥
	encrypted := AesEncryptCBC(origData, keyBytes)
	return base64.StdEncoding.EncodeToString(encrypted)
}

func Decrypt(content, key string) string {
	decodeString, _ := base64.StdEncoding.DecodeString(content)
	keyBytes := []byte(key) // 加密的密钥
	encrypted := AesDecryptCBC(decodeString, keyBytes)
	return string(encrypted)
}

